package com.jxutcm.equipmentMaintance.Bean;

public class EmployeeInfo {
    private String 编号;
    private String 姓名;
    private String 部门;

    public String get编号() {
        return 编号;
    }

    public void set编号(String 编号) {
        this.编号 = 编号;
    }

    public String get姓名() {
        return 姓名;
    }

    public void set姓名(String 姓名) {
        this.姓名 = 姓名;
    }

    public String get部门() {
        return 部门;
    }

    public void set部门(String 部门) {
        this.部门 = 部门;
    }
}
